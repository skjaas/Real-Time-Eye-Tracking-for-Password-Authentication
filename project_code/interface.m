% --- Executes on button press in clear.
function start_Callback(hObject, eventdata, handles)
% hObject    handle to clear (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
global res
res=[]
set(handles.res,'string','');


% --- Executes on button press in start.


% hObject    handle to start (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
imaqreset; %device(camera)reset
Cm=imaq.VideoDevice('winvideo',1,'YUY2_640x480');%image acquisition,adapter,deviceID,size
Cm.ReturnedColorSpace='rgb';%return color
Cm.ReturnedDataType='uint8';%storing type
eD=vision.CascadeObjectDetector('EyePairBig');%eye capturing//edege detection
%%
global flag;
cnt=1;
if strcmpi(get(handles.start,'string'),'START')
   set(handles.start,'string','STOP') ;
   flag=1;
   pause(0.4);
   res=[];
else
   set(handles.start,'string','START') ;
   flag=0;
   pause(0.4); 
end
while flag
im=step(Cm);
bbox=step(eD,im);%crop eye from face
if ~isempty(bbox)

eyepair=imcrop(im,bbox(1,:));
A=rgb2gray(eyepair);

 A=wiener2(A);
 B=imtophat(A,strel('disk',5));
 C=imbothat(A,strel('disk',5)); 
 B=imbinarize(B,graythresh(B));
 C=imbinarize(C,graythresh(C));
 D=C&B;
 D=imdilate(D,strel('disk',4));
 D=imfill(D,'holes');
 D=imerode(D,strel('disk',3));
 D=bwareaopen(D,10);
 stat=regionprops(D,'Centroid');
 if ~isempty(stat)
 cd=stat(1).Centroid;
 tmp(cnt,:)=[cd(1)*10,cd(2)*10];cnt=cnt+1;
 if cnt>50
     xt=tmp(:,1);
     a=length(xt(xt<245));
     b=length(xt(xt>245 & xt<375));
     c=length(xt(xt>375));
     avg=mean(tmp);
     [maxv,idx]=max([a,b,c]);
     res=[res,idx];
     S(1:length(res))='*';
     set(handles.res,'string',S);
     beep;
     res
     cnt=1;
     tmp=[];
 end
 end

end
pause(.2);
end



